/**
 * Type chart map (type, type) => multiplier
 */
export type TypeChart = Map<string, Map<string, number>>;

export const EffectiveMul = 1;
export const SuperEffectiveMul = 2;
export const ResistedMul = 0.5;
export const ImmuneMul = 0;

function invertEffectiveness(effectiveness: number): number {
    if (!effectiveness) {
        return 2;
    }
    return 1 / effectiveness;
}

/**
 * Gets effectiveness
 * @param chart The type chart
 * @param offensiveType The offensive type
 * @param defensiveType The defensive type
 * @param isInverse True if inverse battle
 * @returns The multiplier
 */
export function getEffectivenessMultiplier(chart: TypeChart, offensiveType: string, defensiveType: string, isInverse?: boolean): number {
    offensiveType = toId(offensiveType);
    defensiveType = toId(defensiveType);
    if (!chart.has(defensiveType)) {
        return EffectiveMul;
    }
    const subChart = chart.get(defensiveType);
    if (!subChart.has(offensiveType)) {
        return EffectiveMul;
    }

    if (isInverse) {
        return invertEffectiveness(subChart.get(offensiveType));
    }

    return subChart.get(offensiveType);
}

/**
 * Gets effectiveness (combined)
 * @param chart The type chart
 * @param offensiveTypes List of offensive types
 * @param defensiveTypes List of defensive types
 * @param isInverse True if inverse battle
 * @returns The multiplier
 */
export function getCombinedEffectivenessMultiplier(chart: TypeChart, offensiveTypes: string[], defensiveTypes: string[], isInverse?: boolean): number {
    let mul = 1;

    for (const dt of defensiveTypes) {
        for (const ot of offensiveTypes) {
            mul *= getEffectivenessMultiplier(chart, ot, dt, isInverse);
        }
    }

    return mul;
}


