/**
 * Gets type chart for generation
 * @param gen The generation
 * @returns The type chart
 */
export function getTypeChart(gen: number): TypeChart {
    if (gen === 1) {
        return TypeChartGen1;
    } else if (gen < 6) {
        return TypeChartGen2ToGen5;
    } else {
        return TypeChartGen6To9;
    }
}
