/**
 * Gets base PP of a move
 * @param gen The generation
 * @param move The move name
 * @returns The base PP
 */
export function getMoveBasePP(gen: number, move: string): number {
    if (gen < 1 || gen > LAST_GEN) {
        return 1;
    }

    move = toId(move);

    let pp = 1;

    for (let g = 1; g <= gen; g++) {
        const ppData = PP_DATA.get(g);

        if (!ppData || !ppData.has(move)) {
            continue;
        }

        pp = ppData.get(move);
    }

    return pp;
}

/**
 * Gets PP from base PP
 * @param basePP Base PP
 * @returns Max PP
 */
export function getMaxPPFromBasePP(basePP: number) {
    if (basePP === 1) {
        return 1;
    }

    return Math.floor((basePP * 8) / 5);
}

