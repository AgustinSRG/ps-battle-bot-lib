import { toId } from "../utils/id";
import { LAST_GEN } from "./initializers";

/**
 * Gets the accuracy value of a move
 * @param gen The generation
 * @param move The move name
 * @returns The accuracy (From 0 to 1, if grater than 1, the move cannot miss)
 */
export function getMoveAccuracy(gen: number, move: string): number {
    if (gen < 1 || gen > LAST_GEN) {
        return 1;
    }

    move = toId(move);

    let accuracy = 1;

    for (let g = 1; g <= gen; g++) {
        const ppData = ACCURACY_DATA.get(g);

        if (!ppData || !ppData.has(move)) {
            continue;
        }

        accuracy = ppData.get(move);
    }

    return accuracy;
}

